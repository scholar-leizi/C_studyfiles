#include <stdio.h>
int main()
{
    int a = 7/2;
    printf("%d\n",a);
    //“/”除法取商
    /*
    “/”号的两端都是整数时，执行整数除法运算；“/”号的任意一端是浮点数时，执行浮
    点数除法运算
    */

    int b = 7%2;
    printf("%d\n",b);
    //“%”除法取模（取余运算）

    float c = 7/2.0;
    printf("%.1f\n",c);//“%.1f”——打印浮点数类型数据，保留一位小数
    return 0;
}